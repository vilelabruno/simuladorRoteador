class Little {
    double lastTime, qtdPk, sum;

    public Little() {
        this.lastTime = 0;
        this.qtdPk = 0;
        this.sum = 0;
    }

    public double getLastTime() {
        return lastTime;
    }

    public void setLastTime(double lastTime) {
        this.lastTime = lastTime;
    }

    public double getQtdPk() {
        return qtdPk;
    }

    public void setQtdPk(double qtdPk) {
        this.qtdPk = qtdPk;
    }

    public double getSum() {
        return sum;
    }

    public void setSum(double sum) {
        this.sum = sum;
    }
    
}
